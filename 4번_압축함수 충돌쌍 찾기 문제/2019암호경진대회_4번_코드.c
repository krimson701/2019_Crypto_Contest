uint8_t S(uint8_t x) {
	unsigned char t1 = x >> 4;
	unsigned char t2 = x << 4;
	t2 = t2 >> 4;
	int x1 = (int)t1;
	int x2 = (int)t2;
	return s_box[x1][x2];
}
uint8_t vli_mul(int a, uint8_t xo) {

	if (a == 1) {
		return xo;
	}
	uint8_t x;
	if ((xo >> 7) == 0)
		x = xo << 1;
	else
		x = (xo << 1) ^ 27;

	if (a == 3) {
		x = xo ^ x;
	}
	else if(a!=2)
		printf("Error: a가 2와 3이아닌 잘못된 a * x 유한체 곱입니다.\n");
	return x;
}
void MixColumns(uint8_t *x) {
	int i, j, k;
	uint8_t a[4][4] = {
	{ 0x2, 0x3, 0x1, 0x1},
	{ 0x1, 0x2, 0x3, 0x1},
	{ 0x1, 0x1, 0x2, 0x3},
	{ 0x3, 0x1, 0x1, 0x2 }};
	uint8_t tmp[4];
	for (i = 0; i < 4; i++) {
		uint8_t temp[4] = { 0, };
		for (j = 0; j < 4; j++) {
			temp[j] = vli_mul(a[i][j], x[j]);
		}
		tmp[i] = temp[0] ^ temp[1] ^ temp[2] ^ temp[3];
	}
	for (int i = 0; i < 4; i++)
		x[i] = tmp[i];
}
void F(uint8_t *b, uint8_t *rk, uint8_t *x) {
	// 이때 입력 rk와 x는 8비트짜리 4개(총 32비트 2개)
	uint8_t a[4];
	for (int i = 0; i < 4; i++)
		a[i]=x[i] ^ rk[i];
	for (int i = 0; i < 4; i++)
		b[i] = S(a[i]);
	MixColumns(b);
	return;
}
void Round(uint8_t *Y, uint8_t *rk, uint8_t *X) {
	// 8비트 * 4개 = rk, 8비트 * 16개 = X
	uint8_t tmp_F[4];
	F(tmp_F, rk, X);
	uint8_t tmp[16];
	for (int i = 0; i < 4; i++) {
		tmp[i] = tmp_F[i] ^ X[4 + i];
		tmp[i + 4] = X[8 + i];
		tmp[i + 8] = X[12 + i];
		tmp[i + 12] = X[i];
	}
	for (int i = 0; i < 16; i++)
		Y[i] = tmp[i];
	return 0;
}
void __Round(uint8_t *Y, uint8_t *rk, uint8_t *X) {
	// 8비트 * 4개 = rk, 8비트 * 16개 = X
	uint8_t tmp_F[4];
	F(tmp_F, rk, X);
	uint8_t tmp[16];
	for (int i = 0; i < 4; i++) {
		tmp[i] = X[i];
		tmp[i + 4] = 	tmp_F[i] ^ X[4 + i];
		tmp[i + 8] = X[8 + i];
		tmp[i + 12] = X[12+i];
	}
	for (int i = 0; i < 16; i++)
		Y[i] = tmp[i];
	return 0;
}
void hash(uint8_t *H, int j,uint8_t *m) {
	uint8_t X[16], K[16],tmp[4],B[4];
	uint8_t E[4] = { 0x00, 0x00, 0x00, 0x00 };
	for (int i = 0; i < 4; i++) {
		B[i] = m[i + 4];
	}
	for (int i = 0; i < 16; i++) {
		X[i] = m[i];
		K[i] = m[i + 16];
	}
	uint8_t zero_X[16], zero_K[16];
	for (int i = 0; i < 16; i++) {
		zero_X[i] = X[i];
		zero_K[i] = K[i];
	}
	uint8_t rk[4];
	for (int i = 0; i < j - 1; i++) {
		for (int k = 0; k < 4; k++) 
			rk[k] = K[k]; // rk = 32비트
		Round(X, rk, X);
		uint8_t i_tmp[4];
		for (int k = 0; k < 4; k++)
			i_tmp[k] = i + k;
		Round(K, i_tmp, K);
		
		if (i == 0) {
			for (int k = 0; k < 4; k++)
				X[k] = 0x00;
		}
		
	}
	for (int i = 0; i < 16; i++)
		H[i] = K[i];
	
	for (int k = 0; k < 4; k++)
		rk[k] = K[k];
	__Round(X, rk, X);

	for (int i = 0; i < 16; i++) 
		H[i] = X[i] ^ zero_X[i] ^ zero_K[i];
	
	F(tmp, E, m);
	for (int i = 4; i < 8; i++)
		H[i] = H[i] ^ tmp[i-4] ^ B[i-4];
		
		
}
int compare(uint8_t *key, uint8_t *m) { // KEY 해쉬값과 같은지 비교하는 함수
	int count = 0;
	for (int i = 0; i < 16; i++) {
		if (key[i] == m[i])
			count++;
		else 
			break;
	}

	if (count == 16)
		return 1;
	else
		return 0;
}

